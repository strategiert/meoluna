export interface WorldConfig {
  name: string;
  tagline: string;
  emoji: string;
  primaryColor: string;        // hex, z.B. "#0ea5e9"
  bgGradient: string;          // Tailwind classes, z.B. "from-blue-950 via-blue-900 to-cyan-950"
  cardBg: string;              // Tailwind class, z.B. "bg-blue-900/60"
  accentClass: string;         // Tailwind class f√ºr Buttons, z.B. "bg-sky-500 hover:bg-sky-400"
  hubBgUrl?: string;           // Optional: generiertes Hub-Hintergrundbild (Gemini SVG)
  geminiGraphics?: {
    icons: Array<{ id: string; url: string; purpose: string }>;
    illustrations: Array<{ id: string; url: string; purpose: string }>;
    characters: Array<{ id: string; url: string; purpose: string }>;
    backgrounds: Array<{ id: string; url: string; purpose: string }>;
  };
}

export interface MultipleChoiceChallenge {
  type: 'multiple-choice';
  question: string;
  options: string[];
  correct: number;             // Index in options[]
  xp: number;
  feedbackCorrect: string;
  feedbackWrong: string;
  hints?: {
    level1: string;   // Denkfrage ("Schau dir die Farben an ‚Äî was f√§llt dir auf?")
    level2: string;   // Konkreter Hinweis ("Das gesuchte Tier lebt im Wasser")
    level3: string;   // Fast die L√∂sung ("Es beginnt mit 'F' und hat Schuppen")
  };
}

